from turtle import Turtle
class Ball(Turtle):
    def __init__(self):
        super().__init__()
        self.color("white")
        self.shape("circle")
        self.penup()
        self.setpos((0,0))
        self.dx = 20
        self.dy = 20
        self.move_speed = 0.1

    def move(self):
        new_x = self.xcor() + self.dx
        new_y = self.ycor() + self.dy
        self.setpos(new_x,new_y)

    def bounce(self):
        self.dy *= -1

    def rebound(self):
        self.dx *= -1
        self.move_speed *= 0.9

    def change_direction(self):
        if self.ycor()>280 :
            self.bounce()
        if self.ycor()<-280 :
            self.bounce()

    def game_over(self):
        self.setpos(0,0)
        self.move_speed = 0.1
        self.rebound()












































































































































































































































































































































































